#include "libft.h"

int		ft_isalnum(int ch)
{
	return (((ch >= 'a' && ch <= 'z') ||
						(ch >= 'A' && ch <= 'Z') ||
								(ch >= '0' && ch <= '9')) ? 1 : 0);
}
