#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*str;
	size_t	i;

	if (n != 0)
	{
		str = (char *)s;
		i = 0;
		while (i < n)
		{
			str[i] = '\0';
			i++;
		}
	}
}
